package GeeksForGeeks.Java.Operators;

public class Shift {
    public static void main(String[] args)
    {
        int a = 10;
        
        // using left shift
        System.out.println("a<<1 : " + (a << 1));
        
        // using right shift
        System.out.println("a>>1 : " + (a >> 1));
    }
}
